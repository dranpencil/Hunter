// Converts translations.csv to translations.js so the game works without a web server (file:// CORS workaround).
// Usage: node convert-translations.js
// Run this every time you update translations.csv.

const fs = require('fs');
const path = require('path');

const csvPath = path.join(__dirname, 'translations.csv');
const jsPath = path.join(__dirname, 'translations.js');

if (!fs.existsSync(csvPath)) {
    console.error('translations.csv not found at:', csvPath);
    process.exit(1);
}

const csv = fs.readFileSync(csvPath, 'utf8');

// Convert CSV text to a safe JS string literal using JSON.stringify
// (handles quotes, backslashes, newlines, unicode automatically)
const jsContent =
    '// AUTO-GENERATED from translations.csv by convert-translations.js\n' +
    '// Do not edit this file directly. Edit translations.csv and re-run the converter.\n' +
    'window.__TRANSLATIONS_CSV__ = ' + JSON.stringify(csv) + ';\n';

fs.writeFileSync(jsPath, jsContent, 'utf8');
console.log('OK -> translations.js (' + jsContent.length + ' bytes, source CSV ' + csv.length + ' bytes)');
